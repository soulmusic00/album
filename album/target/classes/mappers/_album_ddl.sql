CREATE DATABASE ALBUM;

USE ALBUM;

CREATE TABLE T_SHOPPING_MEMBER (
MEMBER_ID	VARCHAR(20) PRIMARY KEY,
MEMBER_PASSWORD	VARCHAR(1000),
MEMBER_GENDER	VARCHAR(50),
MEMBER_BIRTH	DATE,
 TEL	VARCHAR(20),
EMAIL	VARCHAR(20),
ADDRESS	VARCHAR(500),
JOIN_DATE DATE
);

CREATE TABlE T_SHOPPING_ALBUM (

ALBUM_ID	INT PRIMARY KEY AUTO_INCREMENT,
ALBUM_TITLE	VARCHAR(50),
ALBUM_SINGER VARCHAR(50),
ALBUM_PRICE	INT,
ALBUM_Label	VARCHAR(50),
ALBUM_SORT	VARCHAR(50),
ALBUM_RELEASED_DATE	DATE,
ALBUM_DELIVERY_INFO	INT,
ALBUM_STATUS VARCHAR(50),
ALBUM_INTRO	VARCHAR(2000)
);

